// Check for out-of-bounds indices
function checkBounds(patterns, maxCol, maxRow, name) {
  console.log(`Checking ${name}...`);
  for (let char in patterns) {
    let pattern = patterns[char];
    for (let row = 0; row < pattern.length; row++) {
      if (row >= maxRow) {
        console.log(`${char}: Row ${row} exceeds max ${maxRow-1}`);
      }
      for (let col = 0; col < pattern[row].length; col++) {
        if (col >= maxCol) {
          console.log(`${char}: Col ${col} exceeds max ${maxCol-1}`);
        }
      }
    }
  }
}

